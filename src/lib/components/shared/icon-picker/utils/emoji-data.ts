/**
 * Get all emojis from all categories
 */
export function getAllEmojis(): string[] {
	return EMOJI_CATEGORIES.flatMap((cat) => cat.emojis);
}

/**
 * Search emojis by Unicode code point or category name
 */
export function searchEmojis(query: string): string[] {
	const lowerQuery = query.toLowerCase();
	const results: string[] = [];

	for (const category of EMOJI_CATEGORIES) {
		// If query matches category name, include all emojis from that category
		if (category.name.toLowerCase().includes(lowerQuery)) {
			results.push(...category.emojis);
		}
	}

	// Deduplicate results
	return [...new Set(results)];
}
